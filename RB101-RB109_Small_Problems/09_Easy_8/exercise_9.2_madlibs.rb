# Small Problems > Easy 8 > Madlibs

puts "It's Madlib time!"
puts "Please enter a noun:"
noun = gets.chomp
puts "Please enter a verb:"
verb = gets.chomp 
puts "Please enter a adverb:"
adverb = gets.chomp 
puts "Please enter a adjective:"
adjective = gets.chomp

puts "Okay, here's your story:"
puts "The #{adjective} skeleton likes to #{verb} #{adverb} while eating a #{noun}!"
